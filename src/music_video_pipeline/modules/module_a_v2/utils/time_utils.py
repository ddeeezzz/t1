"""
文件用途：提供模块A V2通用时间处理工具。
核心流程：统一时间戳保留三位小数。
输入输出：输入浮点时间值，输出归一化时间值。
依赖说明：仅依赖 Python 标准数值类型。
维护说明：本文件只保留时间工具，不承载流程逻辑。
"""


def round_time(time_value: float) -> float:
    """
    功能说明：统一时间戳保留 3 位小数。
    参数说明：
    - time_value: 当前处理的时间戳（秒）。
    返回值：
    - float: 保留三位小数后的时间戳。
    异常说明：异常由调用方或上层流程统一处理。
    边界条件：遵循当前实现中的兜底与裁剪策略。
    """
    return round(float(time_value), 3)
